package CadastrarEstoqueMP;

public class MateriaPrima {
	
	private Integer idMercadoria;
	private Integer idMercadoriaMateriaPrima;
	private Double quantidade;
	
	public Integer getIdMercadoria() {
		return idMercadoria;
	}
	public void setIdMercadoria(Integer idMercadoria) {
		this.idMercadoria = idMercadoria;
	}
	public Integer getIdMercadoriaMateriaPrima() {
		return idMercadoriaMateriaPrima;
	}
	public void setIdMercadoriaMateriaPrima(Integer idMercadoriaMateriaPrima) {
		this.idMercadoriaMateriaPrima = idMercadoriaMateriaPrima;
	}
	public Double getQuantidade() {
		return quantidade;
	}
	public void setQuantidade(Double quantidade) {
		this.quantidade = quantidade;
	}
	
}
